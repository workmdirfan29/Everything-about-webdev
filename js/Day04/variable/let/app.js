/* <----------------------------let---------------------------->
    What it does: let is the modern way to create a variable.
    Where it's used: let is used inside a block of code (like inside a loop or a function). It only works inside the curly braces {} around it.
    Can you change it? Yes! You can change its value later.
    Why it's better than var: It’s safer and easier to use because it doesn’t cause unexpected results.
*/

let age = 25;
age = 26;           // You can change the value
console.log(age);   // Output: 26
